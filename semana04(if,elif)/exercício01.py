valorUm = float(input("Insira o primeiro valor:"))
valorDois = float(input("insira o segundo valor:"))

if valorUm>valorDois:
    print("O valor", valorUm, "é maior que o valor", valorDois)
elif valorUm<valorDois:
    print("O valor", valorUm, "é menor que o valor", valorDois)
else:
    print("Os valores são iguais.") 